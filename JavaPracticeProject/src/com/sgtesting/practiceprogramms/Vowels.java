package com.sgtesting.practiceprogramms;

public class Vowels {

	public static void main(String[] args) {
		char ch='S';
		if(ch=='a'||ch=='A'||ch=='e'||ch=='E'||ch=='i'||ch=='I'||ch=='o'||ch=='O'||ch=='u'||ch=='U')
		{
			System.out.println(ch+" is a vowel");
		}
		else
		{
			System.out.println(ch+" is not vowels");
		}
	}

}
